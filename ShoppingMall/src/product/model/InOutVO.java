package product.model;

public class InOutVO {
	
	private int inout_code;
	private int status;
	private String prod_code;
	private int inout_qty;
	private String inout_exp;
	private String inout_date;
	
	public int getInout_code() {
		return inout_code;
	}
	public void setInout_code(int inout_code) {
		this.inout_code = inout_code;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getProd_code() {
		return prod_code;
	}
	public void setProd_code(String prod_code) {
		this.prod_code = prod_code;
	}
	public int getInout_qty() {
		return inout_qty;
	}
	public void setInout_qty(int inout_qty) {
		this.inout_qty = inout_qty;
	}
	public String getInout_exp() {
		return inout_exp;
	}
	public void setInout_exp(String inout_exp) {
		this.inout_exp = inout_exp;
	}
	public String getInout_date() {
		return inout_date;
	}
	public void setInout_date(String inout_date) {
		this.inout_date = inout_date;
	}
	
}
